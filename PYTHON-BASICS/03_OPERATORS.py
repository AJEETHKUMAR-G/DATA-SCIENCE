#3. OPERATORS:

x = int(input("pick a number:"))
print(x)
y = int(input("pick a number:"))
print(y)

#ARITHEMATIC OPERATOR:

print(x + y) # ADDITION
print(x - y) # SUBRACTION
print(x * y) # MULTIPLICATION
print(x / y) # DIVISION
print(x // y) # INTEGER DIVISION
print(x % y) # MODULO
print(x ** y) # EXPONENTIAL

#ASSIGNMENT OPERATOR:

a = 4
b = 5
c = 7
d = 8
a+=3
print(a)
b-=3
print(b)
c*=3
print(c)
d/=3
print(d)

#COMPARISION OPERATOR:

a = 67
b = 54
print(a==b)
print(a!=b)
print(a>b)
print(a<b)
print(a>=b)
print(a<=b)

#LOGICAL OPERATOR:

a = 67
b = 54
print(a>65 and b<55)
print(a<65 or b<55)
print(not(a>65 and b<55))

#IDENTITY OPERATOR:

x = 6
y = 7
print(x is y)
print(x is not y)

#MEMBERSHIP OPERATOR:

x = 69
y = (4,5,6,7)
print(x in y)
print(3 not in y)

#BITWISE OPERATOR:

x = 6
y = 10
print(x & y)
print(x | y)
print(x ^ y)
print(~x)
print(x<<2)
print(x>>2)

























